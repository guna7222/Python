# Variables are containers for storing data values
# In Python variables are created the moment when you assign a value to it
# Python is a dynamic programming language where you have to explicitly specify the data types of the variable

x = 5
name = "GS"

print(x, name)

# Casting
# if you want to specify the data type of the variable explicitly
x = int(5)
name = str("guna")
decimal_number = float(5.4)

print(x, name, decimal_number)

# type()
# how to check type of variable
# in order to check data type of variable in python we can use type() function
print(type(x))

